# Write a NumPy program to create an array of 10 zeros,10 ones, 10 fives.

import numpy as np
array1=np.zeros(10)
print("array with 10 zeros",array1)
array2=np.ones(10)
print("array with 10 ones",array2)
array3=np.full(10,5)
print("array with 10 fives",array3)
array4=np.ones(10)*5
print("array with 10 fives",array4)
